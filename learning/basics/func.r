# basic functions in r-lang
# print
print("print")
print("Hello, World!")
# paste
print("paste")
paste("Hello", "World!")
# seq
print("seq")
seq(1, 10)
# rep
print("rep")
rep(1, 10)
# c
print("c")
c(1, 2, 3, 4, 5)
# cbind
print("cbind")
cbind(1:5, 6:10)




# what is the difference between print and paste?
# print is a function that prints the output to the console
# paste is a function that concatenates strings together
# seq is a function that generates a sequence of numbers
# rep is a function that replicates a value a specified number of times
# c is a function that combines values into a vector
# cbind is a function that combines vectors into a matrix
